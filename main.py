# Задача:
# Написать программу, которая:
# 1. Создает случайный список из 20 значений, состоящий из 4 случайных целых чисел от -10 до 10.
# 2. Находит все уникальные комбинации пар из этих значений и выводит их в виде списка кортежей.
# 3. Запрашивает у пользователя целое число.
# 4. Вычисляет количество пар, чья сумма меньше заданного пользователем значения.

import random  # Импортируем модуль random для генерации случайных чисел
from itertools import combinations  # Импортируем функцию combinations для создания уникальных пар
numbers = [random.randint(-10, 10) for _ in range(20)] # Генерируем список из 20 элементов, где каждый элемент — это список из 4 случайных чисел от -10 до 10
print("Сгенерированный список чисел:", numbers) # Выводим сгенерированный список чисел
unique_pairs = list(combinations(numbers, 2)) # Находим все уникальные пары (комбинации по 2) из списка numbers
print("Все уникальные комбинации пар:", unique_pairs) # Выводим уникальные пары
while True:
    try:
        threshold = int(input("Введите целое число для сравнения суммы пар: "))  # Запрашиваем у пользователя целое число
        break  # Прерываем цикл, если введено корректное значение
    except ValueError:
        print("Пожалуйста, введите целое число.")  # Выводим сообщение об ошибке при некорректном вводе
count = 0 # Инициализируем счетчик для количества пар, чья сумма меньше заданного значения

for pair in unique_pairs: # Проходим по каждой паре в списке unique_pairs
    if sum(pair) < threshold:  # Если сумма пары меньше введенного пользователем значения
        count += 1  # Увеличиваем счетчик
print(f"Количество пар, сумма которых меньше {threshold}: {count}") # Выводим результат